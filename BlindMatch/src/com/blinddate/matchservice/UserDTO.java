package com.blinddate.matchservice;

public class UserDTO {

	// 상세정보 기입
	private String id;
	private int age;
	private int height;
	private int weight;
	private String mbti;
	private String car;
	private String rel;
	private String drink;
	private String smoke;
	private String gender;
	private String addr;
	private String name;
	private String phoneNum;
	private String matching;
	private String msuccess;
	private String couponNo;
	private int couponDiscount;

	public UserDTO() {
	}

	public UserDTO(String id, int age, int height, int weight, String mbti, String car, String rel, String drink,
			String smoke, String gender, String addr, String name, String phoneNum, String matching, String msuccess,
			String couponNo, int couponDiscount) {
		super();
		this.id = id;
		this.age = age;
		this.height = height;
		this.weight = weight;
		this.mbti = mbti;
		this.car = car;
		this.rel = rel;
		this.drink = drink;
		this.smoke = smoke;
		this.gender = gender;
		this.addr = addr;
		this.name = name;
		this.phoneNum = phoneNum;
		this.matching = matching;
		this.msuccess = msuccess;
		this.couponNo = couponNo;
		this.couponDiscount = couponDiscount;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public String getMbti() {
		return mbti;
	}

	public void setMbti(String mbti) {
		this.mbti = mbti;
	}

	public String getCar() {
		return car;
	}

	public void setCar(String car) {
		this.car = car;
	}

	public String getRel() {
		return rel;
	}

	public void setRel(String rel) {
		this.rel = rel;
	}

	public String getDrink() {
		return drink;
	}

	public void setDrink(String drink) {
		this.drink = drink;
	}

	public String getSmoke() {
		return smoke;
	}

	public void setSmoke(String smoke) {
		this.smoke = smoke;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhoneNum() {
		return phoneNum;
	}

	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}

	public String getMatching() {
		return matching;
	}

	public void setMatching(String matching) {
		this.matching = matching;
	}

	public String getMsuccess() {
		return msuccess;
	}

	public void setMsuccess(String msuccess) {
		this.msuccess = msuccess;
	}

	public String getCouponNo() {
		return couponNo;
	}

	public void setCouponNo(String couponNo) {
		this.couponNo = couponNo;
	}

	public int getCouponDiscount() {
		return couponDiscount;
	}

	public void setCouponDiscount(int couponDiscount) {
		this.couponDiscount = couponDiscount;
	}

	@Override
	public String toString() {
		return "UserDTO [id=" + id + ", age=" + age + ", height=" + height + ", weight=" + weight + ", mbti=" + mbti
				+ ", car=" + car + ", rel=" + rel + ", drink=" + drink + ", smoke=" + smoke + ", gender=" + gender
				+ ", addr=" + addr + ", name=" + name + ", phoneNum=" + phoneNum + ", matching=" + matching
				+ ", msuccess=" + msuccess + ", couponNo=" + couponNo + ", couponDiscount=" + couponDiscount + "]";
	}

	

} // class 끝
