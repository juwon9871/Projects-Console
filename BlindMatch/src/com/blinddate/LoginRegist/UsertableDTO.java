package com.blinddate.LoginRegist;
public class UsertableDTO {
	private String id; 
	private String pwd;
	private String name;
	private String phoneNum;
	private String gender;
	
	public UsertableDTO() {
	}

	public UsertableDTO(String id, String pwd, String name, String phoneNum, String gender) {
		super();
		this.id = id;
		this.pwd = pwd;
		this.name = name;
		this.phoneNum = phoneNum;
		this.gender = gender;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhoneNum() {
		return phoneNum;
	}

	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	@Override
	public String toString() {
		return "UsertableDTO [id=" + id + ", pwd=" + pwd + ", name=" + name + ", phoneNum=" + phoneNum + ", gender="
				+ gender + "]";
	}
}	