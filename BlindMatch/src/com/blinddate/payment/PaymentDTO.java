package com.blinddate.payment;

public class PaymentDTO {
	private String name;
	private String id;
	private String pwd;
	private int pay;
	private int monthlyPay;
	private String payCheck;
	private String payDate;
	private String coupon;
	private String adminAccount;
	
	@Override
	public String toString() {
		return "PaymentDTO [name=" + name + ", id=" + id + ", pwd=" + pwd + ", pay=" + pay + ", monthlyPay="
				+ monthlyPay + ", payCheck=" + payCheck + ", payDate=" + payDate + ", coupon=" + coupon
				+ ", adminAccount=" + adminAccount + "]";
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public int getPay() {
		return pay;
	}

	public void setPay(int pay) {
		this.pay = pay;
	}

	public int getMonthlyPay() {
		return monthlyPay;
	}

	public void setMonthlyPay(int monthlyPay) {
		this.monthlyPay = monthlyPay;
	}

	public String getPayCheck() {
		return payCheck;
	}

	public void setPayCheck(String payCheck) {
		this.payCheck = payCheck;
	}

	public String getPayDate() {
		return payDate;
	}

	public void setPayDate(String payDate) {
		this.payDate = payDate;
	}

	public String getCoupon() {
		return coupon;
	}

	public void setCoupon(String coupon) {
		this.coupon = coupon;
	}

	public String getAdminAccount() {
		return adminAccount;
	}

	public void setAdminAccount(String adminAccount) {
		this.adminAccount = adminAccount;
	}

	public PaymentDTO() {
		
	}
	
	public PaymentDTO(String name, String id, String pwd, int pay, int monthlyPay, String payCheck, String payDate,
			String coupon, String adminAccount) {
		super();
		this.name = name;
		this.id = id;
		this.pwd = pwd;
		this.pay = pay;
		this.monthlyPay = monthlyPay;
		this.payCheck = payCheck;
		this.payDate = payDate;
		this.coupon = coupon;
		this.adminAccount = adminAccount;
	}
	
}